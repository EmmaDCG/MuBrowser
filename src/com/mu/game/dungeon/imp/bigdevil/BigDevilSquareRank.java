package com.mu.game.dungeon.imp.bigdevil;

public class BigDevilSquareRank {
   private int rank;
   private int minKill;
   private int maxKill;
   private String target;

   public int getRank() {
      return this.rank;
   }

   public void setRank(int rank) {
      this.rank = rank;
   }

   public int getMinKill() {
      return this.minKill;
   }

   public void setMinKill(int minKill) {
      this.minKill = minKill;
   }

   public int getMaxKill() {
      return this.maxKill;
   }

   public void setMaxKill(int maxKill) {
      this.maxKill = maxKill;
   }

   public String getTarget() {
      return this.target;
   }

   public void setTarget(String target) {
      this.target = target;
   }
}
