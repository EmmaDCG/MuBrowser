package com.mu.game.model.activity;

public class ActivityRoleLogs {
   private int eid;
   private String date;
   private long rid;

   public int getEid() {
      return this.eid;
   }

   public void setEid(int eid) {
      this.eid = eid;
   }

   public String getDate() {
      return this.date;
   }

   public void setDate(String date) {
      this.date = date;
   }

   public long getRid() {
      return this.rid;
   }

   public void setRid(long rid) {
      this.rid = rid;
   }
}
